function detectLang() {
  const saved = localStorage.getItem("opens2p_lang");
  if (saved && translations[saved]) return saved;
  const browserLangs = navigator.languages || [navigator.language || "en"];
  for (const bl of browserLangs) {
    const code = bl.toLowerCase().split("-")[0];
    if (translations[code]) return code;
  }
  return "en";
}

function applyLang(lang) {
  if (!translations[lang]) lang = "en";
  const dict = translations[lang];
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("dir", RTL_LANGS.has(lang) ? "rtl" : "ltr");
  localStorage.setItem("opens2p_lang", lang);
  const switcher = document.getElementById("lang-switcher");
  if (switcher) switcher.value = lang;
}

function initLangSwitcher() {
  const switcher = document.getElementById("lang-switcher");
  if (!switcher) return;
  Object.entries(SUPPORTED_LANGS).forEach(([code, label]) => {
    const opt = document.createElement("option");
    opt.value = code;
    opt.textContent = label;
    switcher.appendChild(opt);
  });
  switcher.addEventListener("change", e => applyLang(e.target.value));
}

document.addEventListener("DOMContentLoaded", () => {
  initLangSwitcher();
  applyLang(detectLang());
});
